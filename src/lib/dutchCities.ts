// Lookup set for O(1) validation (lowercase slug → proper name)
const slugToCity = new Map<string, string>();
for (const city of DUTCH_CITIES) {
  const slug = city
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-");
  slugToCity.set(slug, city);
}

/**
 * Check if a slug corresponds to a known Dutch city/place.
 * Returns the proper city name if valid, undefined otherwise.
 */
export const getValidCityName = (slug: string): string | undefined => {
  return slugToCity.get(slug.toLowerCase());
};

/**
 * Check if a slug is a valid Dutch city.
 */
export const isValidDutchCity = (slug: string): boolean => {
  return slugToCity.has(slug.toLowerCase());
};
